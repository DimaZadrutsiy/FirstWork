public class CreateArray {

    public int[] createArray(int num, int num2, int num3, int num4, int num5){
        return new int[]{num, num2, num3, num4, num5};
    }


    public double[] createDoubleArray(double num, double num2, double num3, double num4, double num5){
        return new double[]{num, num2, num3, num4, num5};
    }


    public String[] createStringArray(String num, String num2, String num3, String num4, String num5){
        return new String[]{num, num2, num3, num4, num5};
    }



}
